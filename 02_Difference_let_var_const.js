// 1) avoid var because it can cause bugs in program due to some reasons.

// 2) var is pre ES6 keyword for declaring variable.

// 3) let and const are ES6 keyword for declaring variables so always use them instead of var.

// 4) const is used to decalre constants and const (constants) are those identifiers in which we cannot change the value of const variable throughout the javascript program.

// 5) let is used to declare block scoped variables.

// 6) We should mostly try to use const identifier for declaring variables.

// 7) var is globally scoped but let and const are block scoped.

// 8) var can be updated and redeclared within its scope.

// 9) let can be updated but not redeclared.

// 10) const can neither be updated nor redeclared.

// 11) {}, if{}, else{}, function(not this){} etc. etc. create a new scope and block.

// 12) Var variables are initialized with undefined whereas let and const variables are not initialized.
// 13) Const must be initialized during declaration unlike let and var.

// 14) If you know that a variable will not change its value then declare it as a const otherwise use let and avoid var.



